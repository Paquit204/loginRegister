    private void ptableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ptableMouseClicked
          
           int rowIndex = ptable.getSelectedRow();
        if(rowIndex < 0){
            JOptionPane.showMessageDialog(null,"Please select a Pet");
        }else {
            
             TableModel model= ptable.getModel();
             adoptionForm adb = new  adoptionForm();   
             adb.pid.setText(""+ model.getValueAt(rowIndex, 0));
              adb.ptype.setSelectedItem(""+ model.getValueAt(rowIndex, 1));
              
          
              adb.setVisible(true);
              this.dispose();
        
        
    }                                   
        
    }//GEN-LAST:event_ptableMouseClicked

